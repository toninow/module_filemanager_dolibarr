/**
 * FunciÃ³n principal de traducciÃ³n
 */
function t($key, $default = '') {
    $language = getFileManagerLanguage();
    $translations = getFileManagerTranslations();
    
    if (isset($translations[$language][$key])) {
        return $translations[$language][$key];
    }
    
    // Si no existe en el idioma actual, intentar inglÃ©s
    if ($language !== 'en' && isset($translations['en'][$key])) {
        return $translations['en'][$key];
    }
    
    // Si no existe, devolver el key o el default
    return !empty($default) ? $default : $key;
}

