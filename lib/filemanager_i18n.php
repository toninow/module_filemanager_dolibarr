/**
 * Obtener el idioma actual del usuario
 */
function getFileManagerLanguage() {
    global $db, $conf;
    static $language_cache = null;
    
    if ($language_cache !== null) {
        return $language_cache;
    }
    
    $default_language = 'en'; // InglÃ©s por defecto
    
    // Intentar obtener desde la sesiÃ³n primero
    if (session_status() === PHP_SESSION_ACTIVE && isset($_SESSION['filemanager_language'])) {
        $language_cache = $_SESSION['filemanager_language'];
        return $language_cache;
    }
    
    // Intentar obtener desde la base de datos
    if (isset($db) && $db && !empty($db->database_name)) {
        try {
            $entity = isset($conf->entity) ? (int)$conf->entity : 0;
            $sql = "SELECT value FROM " . MAIN_DB_PREFIX . "const WHERE entity = " . $entity . " AND name = 'FILEMANAGER_LANGUAGE' LIMIT 1";
            $resql = $db->query($sql);
            if ($resql && $db->num_rows($resql) > 0) {
                $obj = $db->fetch_object($resql);
                if (!empty($obj->value)) {
                    $language_cache = $obj->value;
                    // Guardar en sesiÃ³n para futuras consultas
                    if (session_status() === PHP_SESSION_ACTIVE) {
                        $_SESSION['filemanager_language'] = $language_cache;
                    }
                    return $language_cache;
                }
            }
        } catch (Exception $e) {
            error_log("Error getting language: " . $e->getMessage());
        }
    }
    
    $language_cache = $default_language;
    return $language_cache;
}

/**
 * Guardar el idioma seleccionado
 */
function saveFileManagerLanguage($language) {
    global $db, $conf;
    
    if (!in_array($language, ['en', 'es', 'fr', 'de'])) {
        $language = 'en';
    }
    
    // Guardar en sesiÃ³n
    if (session_status() === PHP_SESSION_ACTIVE) {
        $_SESSION['filemanager_language'] = $language;
    }
    
    // Guardar en base de datos
    if (isset($db) && $db && !empty($db->database_name)) {
        try {
            $entity = isset($conf->entity) ? (int)$conf->entity : 0;
            
            // Verificar si ya existe
            $sql_check = "SELECT COUNT(*) as count FROM " . MAIN_DB_PREFIX . "const WHERE entity = " . $entity . " AND name = 'FILEMANAGER_LANGUAGE'";
            $resql_check = $db->query($sql_check);
            $exists = false;
            if ($resql_check) {
                $obj = $db->fetch_object($resql_check);
                $exists = ($obj->count > 0);
            }
            
            if ($exists) {
                // Actualizar
                $sql = "UPDATE " . MAIN_DB_PREFIX . "const SET value = '" . $db->escape($language) . "' WHERE entity = " . $entity . " AND name = 'FILEMANAGER_LANGUAGE'";
            } else {
                // Insertar
                $sql = "INSERT INTO " . MAIN_DB_PREFIX . "const (entity, name, value, type, visible, note) VALUES (" . $entity . ", 'FILEMANAGER_LANGUAGE', '" . $db->escape($language) . "', 'chaine', 0, 'FileManager Language')";
            }
            
            $db->query($sql);
        } catch (Exception $e) {
            error_log("Error saving language: " . $e->getMessage());
        }
    }
}

